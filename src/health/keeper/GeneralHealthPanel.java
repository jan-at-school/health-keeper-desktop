    //For some past day
   private void resetDialogBoxFor(Day focusedDay){
       HealthKeeper.centreWindow(this.jDialogLogDay);
       this.jTextFieldDate.setText(focusedDay.date);
       this.jTextFieldDate.setEnabled(false);
       this.jTextFieldActivity.setText(focusedDay.activity);
       this.jTextFieldActivity.setEnabled(false);
       this.jTextFieldFood.setText(focusedDay.foods);
       this.jTextFieldFood.setEnabled(false);
       this.jTextFieldCin.setText(focusedDay.calorieIntake+"");
       this.jTextFieldCin.setEnabled(false);
       this.jTextFieldCout.setText(focusedDay.calorieBurnt+"");
       this.jTextFieldCout.setEnabled(false);
       this.jButtonSaveDay.setEnabled(false);
       
   }
   //makes text fields empty
   private void refreshDialogBox(){
       HealthKeeper.centreWindow(this.jDialogLogDay);
       this.jTextFieldDate.setText("");
       this.jTextFieldDate.setEnabled(true);
       this.jTextFieldActivity.setText("");
       this.jTextFieldActivity.setEnabled(true);
       this.jTextFieldFood.setText("");
       this.jTextFieldFood.setEnabled(true);
       this.jTextFieldCin.setText("");
       this.jTextFieldCin.setEnabled(true);
       this.jTextFieldCout.setText("");
       this.jTextFieldCout.setEnabled(true);
       this.jButtonSaveDay.setEnabled(true);
   }
    
    
