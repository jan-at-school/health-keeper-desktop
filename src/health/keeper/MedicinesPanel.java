    public void start(){
        
        this.suggestedMedicines=new ArrayList<Medicine>();
        this.previousMedicines=new ArrayList<Medicine>();
        
        for(int i=0; i<user.medicines.size();i++){
            if(user.medicines.get(i).isSuggested){
                this.suggestedMedicines.add(user.medicines.get(i));
            }
            else{
                this.previousMedicines.add(user.medicines.get(i));
            }
        }
        
        this.namesOfSuggested = new String[this.suggestedMedicines.size()];
        this.namesOfPrevious = new String[this.previousMedicines.size()];
        
        
        System.out.println(namesOfSuggested.length+"   "+namesOfPrevious.length);
        for(int i=0; i<this.suggestedMedicines.size();i++){
            this.namesOfSuggested[i] = this.suggestedMedicines.get(i).name;
        }
        for(int i=0; i<this.previousMedicines.size();i++){
            if(previousMedicines.get(i).isForbidden){
                this.namesOfPrevious[i] = "(Forbidden) "+this.previousMedicines.get(i).name;
            }
            else{
                this.namesOfPrevious[i] = this.previousMedicines.get(i).name;
            }
        }
        
        if(namesOfPrevious.length>0){
            this.jListPrevious.setListData(namesOfPrevious);
        }
        else{
            this.jListPrevious.setListData(new String[]{"empty here"});
        }
        
        if(namesOfSuggested.length>0){
            this.jListNewMedicines.setListData(namesOfSuggested);
        }
        else{
            this.jListNewMedicines.setListData(new String[]{"empty here"});
        }
        
        
        
        
        if(HealthKeeper.isUser()){
            this.jButtonSuggestMedicine.setEnabled(false);
        }
    }

