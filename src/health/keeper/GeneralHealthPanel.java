    private void jListDaysMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListDaysMouseClicked
        if(datesInStrings.length==0){
            return;
        }
        if (evt.getClickCount() == 2) {
            int focusedIndex = this.jListDays.locationToIndex(evt.getPoint());
            this.focusedDay =user.days.get(focusedIndex);
            resetDialogBoxFor(focusedDay);
            this.jDialogLogDay.setVisible(true);
            
        }
    }//GEN-LAST:event_jListDaysMouseClicked

    private void jButtonSaveDayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveDayActionPerformed
        String date = this.jTextFieldDate.getText();
        String activity = this.jTextFieldActivity.getText();
        String caloriein = this.jTextFieldCin.getText();
        String caloriout = this.jTextFieldCout.getText();
        String food= this.jTextFieldActivity.getText();
        
        user.days.add(new Day(date,caloriein,food,activity,caloriout));
        
        this.jDialogLogDay.setVisible(false);
        start();
        
    }//GEN-LAST:event_jButtonSaveDayActionPerformed

