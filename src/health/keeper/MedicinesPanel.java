    private void jButtonMedicalShopReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMedicalShopReportActionPerformed
        
        
        String bill="NOTE: ENTER YES WHEN YOU BUY THE MEDICINES\n";
        for(int i=0;i<this.suggestedMedicines.size();i++){
            String name =this.suggestedMedicines.get(i).name;
            int totalQuantity=this.suggestedMedicines.get(i).quatitityPerDay*this.suggestedMedicines.get(i).duration;
            bill+=totalQuantity+ " "+ this.suggestedMedicines.get(i).type+" of "+name+"\n";
        }
        
        
        
        int confirmed = JOptionPane.showConfirmDialog(null,
                bill, "Medical Shop Bill",
                JOptionPane.YES_NO_OPTION);

        if (confirmed == JOptionPane.YES_OPTION) {
            for(int i=0; i<this.suggestedMedicines.size();i++){
                this.suggestedMedicines.get(i).isSuggested=false;
                this.previousMedicines.add(this.suggestedMedicines.get(i));
                this.suggestedMedicines.get(i).startDate=new Date();
                this.suggestedMedicines.remove(i);
            }
        }
       start();
    }//GEN-LAST:event_jButtonMedicalShopReportActionPerformed

