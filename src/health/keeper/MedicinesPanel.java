    //make the medicince forbidden by setting the forbidden field as true
    private void jButtonForbidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonForbidActionPerformed
        this.jDialogMedicineData.setAlwaysOnTop(false);
        int confirmed = JOptionPane.showConfirmDialog(null,
                "This is irreversible, do you still want to continue?", "Confirm?",
                JOptionPane.YES_NO_OPTION);

        if (confirmed == JOptionPane.YES_OPTION) {
            this.focusedMedicine.isForbidden=true;
            this.focusedMedicine.isSuggested=false;
        }
        
        
        
        start();
        this.refreshMedicineDataDialogFor(focusedMedicine);
        this.jDialogMedicineData.setAlwaysOnTop(true);
    }//GEN-LAST:event_jButtonForbidActionPerformed


    
    //update jlabels and stuff to display info of medicine....
    public void refreshMedicineDataDialogFor(Medicine medicine){
        HealthKeeper.centreWindow(this.jDialogMedicineData);
        this.jLabelMedicineName.setText("Medicine Name: "+medicine.name);
        
        if(medicine.isSuggested){
            this.jLabelStartDate.setText("Start Date: ----haven't started yet----");
        }
        else{
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            this.jLabelStartDate.setText("Start Date: "+dateFormat.format(new Date()));
        }
        
        this.jLabelSuggestedBy.setText("Prescribed By: "+medicine.suggestedBy.getName());
        this.jLabelQuantity.setText("Quantity: "+ medicine.quatitityPerDay+medicine.type+" per day");
        this.jLabelDuration.setText("Duration: "+medicine.duration +" days");
        this.jTextAreaCommentPrevious.setText(medicine.comment);
        this.jTextAreaCommentPrevious.setEnabled(false);
        if(medicine.isForbidden){
            this.jButtonForbid.setEnabled(false);
            this.jButtonForbid.setText("Forbidden now...");
        }
        else{
            this.jButtonForbid.setEnabled(true);
            this.jButtonForbid.setText("Forbid now");
        }
    }
    
