    public void start(){
        //blocking some fuctionality for doctors
        if(!HealthKeeper.isUser()){
            this.jButtonLogDay.setEnabled(false);
            this.jButtonLogHR.setEnabled(false);
            this.jButtonLogDay.setEnabled(false);
            
        }
        
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            
        datesInStrings=new String[user.days.size()];
        for(int i=0; i<user.days.size();i++){
            datesInStrings[i]=user.days.get(i).date;
        }
        if(datesInStrings.length>0){
            this.jListDays.setListData(datesInStrings);
        }
        else{
            this.jListDays.setListData(new String[]{"empty here"});
        }
        
        
        //setScores function tells the graphpanel to update graph for the data (arrayList of double)
        hr.setScores(user.heartRateLog);
        wt.setScores(user.weightLog);
        
        
        
        this.jLabelGeneralName.setText("Name: "+user.name);
        this.jLabelGeneralAge.setText("Age: "+ user.age);
        this.jLabelGeneralSex.setText("Sex: "+ user.sex);
        this.jLabelGeneralAllergies.setText("Other Info: "+ user.info);
        
        
    }
